public class DoBee {
    public static void main(String[] args) {
        int i = 1;
        while (i < 3) {
            System.out.print("Doo");
            System.out.print("Bee");
            i += 1;
        }
        if (i == 3) {
            System.out.println("Do");
        }

    }
}

/*  output:
        DooBeeDooBeeDo
 */
